#2. Faça um programa que:
#-receba quatro números inteiros
#-calcule e mostre a soma desses números.
# LEIA n1, n2, n3, n4
# soma = n1 + n2 + n3 + n4
# ESCREVA soma
#entrada
n1 = int(input('Informe o primeiro númeroa : '))
n2 = int(input('Informe o segundo número: '))
